--הגדרת טבלאות

CREATE TABLE Movies --טבלה ראשית שמרכזת מידע על הסרטים, לכל סרט יש מספר מזהה משלו אשר משמש כמפתח ראשי
(
 MovieID INT
         CONSTRAINT PK_Movies PRIMARY KEY,
 Title VARCHAR (100)
       CONSTRAINT NN_Title NOT NULL,
 Runtime INT,
 ReleaseDate DATE
             CONSTRAINT NN_ReleaseDate NOT NULL,
 VoteCount INT,
 VoteAverage DECIMAL (5,1),
 Budget MONEY,
 Revenue MONEY
 CONSTRAINT UK_Title_ReleaseDate UNIQUE (Title, ReleaseDate) --כדי להימנע מהכנסת בדיוק אותו סרט פעמיים, שילוב של שם הסרט ותאריך היציאה שלו הם מפתח ייחודי. יש מקרים ששני סרטים עם בדיוק אותו שם יצאו בדיוק באותה שנה, אך הסיכוי שהם יצאו בדיוק באותו תאריך כמעט בלתי אפשרי
)

CREATE TABLE Actors --טבלה ראשית שמרכזת את השחקנים, לכל שחקן יש מספר מזהה משלו אשר משמש כמפתח ראשי
(
 ActorID INT
         CONSTRAINT PK_Actors PRIMARY KEY,
 Actor VARCHAR (100)
       CONSTRAINT NN_Actor NOT NULL
)

CREATE TABLE Movies_Actors --טבלה מקשרת בין טבלאות סרטים ושחקנים, מפתח ראשי הוא שילוב של מספר מזהה של הסרט ומספר מזהה של השחקן
(
 MovieID INT NOT NULL,
 ActorID INT NOT NULL,
 CONSTRAINT PK_Movies_Actors PRIMARY KEY (MovieID, ActorID),
 CONSTRAINT FK_MovAct_Mov_MovieID FOREIGN KEY (MovieID) REFERENCES Movies (MovieID), --מפתח זר שמקשר למספר מזהה של הסרט בטבלת סרטים
 CONSTRAINT FK_MovAct_Act_ActorID FOREIGN KEY (ActorID) REFERENCES Actors (ActorID) --מפתח זר שמקשר למספר מזהה של השחקן בטבלת שחקנים
)

CREATE TABLE Directors --טבלה ראשית שמרכזת את הבמאים, לכל במאי יש מספר מזהה משלו אשר משמש כמפתח ראשי
(
 DirectorID INT
            CONSTRAINT PK_Directors PRIMARY KEY,
 Director VARCHAR (100)
          CONSTRAINT NN_Director NOT NULL
)

CREATE TABLE Movies_Directors --טבלה מקשרת בין טבלאות סרטים ובמאים, מפתח ראשי הוא שילוב של מספר מזהה של הסרט ומספר מזהה של הבמאי
(
 MovieID INT NOT NULL,
 DirectorID INT NOT NULL,
 CONSTRAINT PK_Movies_Directors PRIMARY KEY (MovieID, DirectorID),
 CONSTRAINT FK_MovDir_Mov_MovieID FOREIGN KEY (MovieID) REFERENCES Movies (MovieID), --מפתח זר שמקשר למספר מזהה של הסרט בטבלת סרטים
 CONSTRAINT FK_MovDir_Dir_DirectorID FOREIGN KEY (DirectorID) REFERENCES Directors (DirectorID) --מפתח זר שמקשר למספר מזהה של הבמאי בטבלת במאים
)

CREATE TABLE Genres --טבלה ראשית שמרכזת את הז'אנרים, לכל ז'אנר יש מספר מזהה משלו אשר משמש כמפתח ראשי
(
 GenreID INT
         CONSTRAINT PK_Genres PRIMARY KEY,
 Genre VARCHAR (100)
       CONSTRAINT NN_Genre NOT NULL
)

CREATE TABLE Movies_Genres --טבלה מקשרת בין טבלאות סרטים וז'אנרים, מפתח ראשי הוא שילוב של מספר מזהה של הסרט ומספר מזהה של הז'אנר
(
 MovieID INT NOT NULL,
 GenreID INT NOT NULL,
 CONSTRAINT PK_Movies_Genres PRIMARY KEY (MovieID, GenreID),
 CONSTRAINT FK_MovGen_Mov_MovieID FOREIGN KEY (MovieID) REFERENCES Movies (MovieID), --מפתח זר שמקשר למספר מזהה של הסרט בטבלת סרטים
 CONSTRAINT FK_MovGen_Gen_GenreID FOREIGN KEY (GenreID) REFERENCES Genres (GenreID) --מפתח זר שמקשר למספר מזהה של הז'אנר בטבלת ז'אנרים
)

CREATE TABLE Studios --טבלה ראשית שמרכזת את האולפנים, לכל אולפן יש מספר מזהה משלו אשר משמש כמפתח ראשי
(
 StudioID INT
          CONSTRAINT PK_Studios PRIMARY KEY,
 Studio VARCHAR (100)
        CONSTRAINT NN_Studio NOT NULL
)

CREATE TABLE Movies_Studios --טבלה מקשרת בין טבלאות סרטים ואולפנים, מפתח ראשי הוא שילוב של מספר מזהה של הסרט ומספר מזהה של האולפן
(
 MovieID INT NOT NULL,
 StudioID INT NOT NULL,
 CONSTRAINT PK_Movies_Studios PRIMARY KEY (MovieID, StudioID),
 CONSTRAINT FK_MovStu_Mov_MovieID FOREIGN KEY (MovieID) REFERENCES Movies (MovieID), --מפתח זר שמקשר למספר מזהה של הסרט בטבלת סרטים
 CONSTRAINT FK_MovStu_Stu_StudioID FOREIGN KEY (StudioID) REFERENCES Studios (StudioID) --מפתח זר שמקשר למספר מזהה של האולפן בטבלת אולפנים
)



